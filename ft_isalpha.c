#include "libft.h"

int	ft_isalpha(char *str)
{
	while (*str)
	{
		if ((*str >= 101 && *str <= 132) || (*str >= 65 && *str <= 90))
		{
			return (0);
		}
		str++;
	}
	return (1);
}
