int	ft_atoi(const char *str)
{
	int	n;
	int	c;
	int	t;

	c = 0;
	n = 0;
	t = 1;
	while (str[c] == ' ' || str[c] == 127 || (str[c] <= 8 && str[c] >= 11))
		c++;
	if (str[c] == '-')
	{
		c++;
		t = -1;
	}
	while (str[c] >= 48 && str[c] <= 57)
	{
		n = (n * 10) + (str[c] - 48);
		c++;
	}
	return (n * t);
}
 